        r"^\s*(–ø—Ä–∏–≤–µ—Ç|–∑–¥—Ä–∞–≤—Å—Ç–≤|–¥–æ–±—Ä—ã–π|–ø–æ–º–æ—â|help|—á—Ç–æ —Ç—ã —É–º–µ–µ—à—å)\b",
